package com.TestProject;

public class For_Loop_JavaEx2 {
	
	public static void main(String[] args) {
		
		for (int i =2; i <10; i=i+2)
		{
		System.out.println("For loop: "+ i);	
		}
				
		for (int q = 10; q > 0; q--)
		{
			System.out.println("Reverse Loop: "+ q);
		}
		String m = "Muhammad Ali";
		System.out.println("length of string m: "+ m.length());
				
		int w = 29;
		//
		for (int i = 2; i < w; i++)
		{
			System.out.println(i);
		}
	}
	
}